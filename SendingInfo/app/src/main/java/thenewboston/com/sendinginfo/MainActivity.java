    public void sendMail(View view)throws android.content.ActivityNotFoundException
    {
        String Mail="";
        Intent emailIntent=new Intent(Intent.ACTION_SEND);



        et1=(EditText)findViewById(R.id.et1);
        et2=(EditText)findViewById(R.id.et2);
        et3=(EditText)findViewById(R.id.et3);
        et4=(EditText)findViewById(R.id.et4);

        Mail+="Person1:"+et1.getText().toString()+"\n"+"Person2:"+et2.getText().toString()+"\n"+"Person3:"+et3.getText().toString()+"\n"+"Person4:"+et4.getText().toString()+"\n";
        emailIntent.setData(Uri.parse("mailto..."));
        emailIntent.setType("text/plain");
        emailIntent.putExtra(Intent.EXTRA_TEXT, Mail);

        startActivity(Intent.createChooser(emailIntent, "Complete Action Using:"));

    }

